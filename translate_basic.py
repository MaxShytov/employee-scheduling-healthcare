def translate_po_file(lang_code):
    """Add translations to a .po file."""
    po_file = Path(f'locale/{lang_code}/LC_MESSAGES/django.po')

    if not po_file.exists():
        print(f"File not found: {po_file}")
        return

    content = po_file.read_text(encoding='utf-8')
    translations = TRANSLATIONS.get(lang_code, {})

    modified = False
    for english, translated in translations.items():
        # Pattern to find msgid and its empty msgstr
        pattern = rf'msgid "{re.escape(english)}"\nmsgstr ""'
        replacement = f'msgid "{english}"\nmsgstr "{translated}"'

        new_content = re.sub(pattern, replacement, content)
        if new_content != content:
            content = new_content
            modified = True
            print(f"  [{lang_code}] {english} → {translated}")

    if modified:
        po_file.write_text(content, encoding='utf-8')
        print(f"\n✓ Updated {po_file}")
    else:
        print(f"✗ No changes made to {po_file}")


if __name__ == '__main__':
    print("Adding basic translations to .po files...\n")

    for lang in ['fr', 'de', 'it', 'es']:
        print(f"\n{'='*60}")
        print(f"Processing {lang.upper()}...")
        print('='*60)
        translate_po_file(lang)

    print("\n" + "="*60)
    print("Done! Run 'python manage.py compilemessages' to compile.")
    print("="*60)
